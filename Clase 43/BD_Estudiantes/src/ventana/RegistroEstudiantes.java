    private void btnPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPDFActionPerformed
        try {
            Document documento = new Document();
            PdfWriter.getInstance(documento, new FileOutputStream("C:/Users/ANTHONY/documents/NetBeansProjects/Clase 43/BD_Estudiantes/New PDF/ReporteDatos.pdf"));

            //Colocamos una imagen en el archivo pdf
            Image imagen = Image.getInstance("src/img/hack.png");
            imagen.scaleToFit(1000, 150);
            imagen.setAlignment(Image.ALIGN_CENTER);

            //Insertando texto editado en el archivo pdf
            Paragraph texto1 = new Paragraph();
            Paragraph texto2 = new Paragraph();

            texto1.setAlignment(Paragraph.ALIGN_JUSTIFIED);
            texto1.setFont(FontFactory.getFont("Cambria", 20, Font.BOLD, BaseColor.BLACK));
            texto1.add("\n\n Tabla informativa generada por Gianmarco Contreras \n\n");

            texto2.setAlignment(Paragraph.ALIGN_CENTER);
            texto2.setFont(FontFactory.getFont("Cambria", 20, Font.BOLDITALIC, BaseColor.BLUE));
            texto2.add("\n GCP Copyright ©");

            documento.open();
            documento.add(imagen);
            documento.add(texto1);

            //FileOutputStream: para escribir datos en un archivo
            PdfPTable tabla = new PdfPTable(4);
            tabla.addCell("Código");
            tabla.addCell("Nombre");
            tabla.addCell("Grado");
            tabla.addCell("Sección");

            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/practicando", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from estudiantes");
            ResultSet rs = pst.executeQuery();

            if (rs.next() == true) {
                do {
                    tabla.addCell(rs.getString(1));
                    tabla.addCell(rs.getString(2));
                    tabla.addCell(rs.getString(3));
                    tabla.addCell(rs.getString(4));
                } while (rs.next() == true);
            }
            documento.add(tabla);
            documento.add(texto2);
            documento.close();
            cn.close();
            pst.close();
            JOptionPane.showMessageDialog(null, "El reporte fue creado con éxito.");
        } catch (DocumentException | HeadlessException | FileNotFoundException | SQLException e) {

        } catch (IOException e) {

        }
    }//GEN-LAST:event_btnPDFActionPerformed

