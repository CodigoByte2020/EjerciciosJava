    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        //Falta validar que el usuario llene todos los datos para poder registrar correctamente        
        try {
            //Realizamos la conexión con la base de datos
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/practicando", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into estudiantes values(?, ?, ?, ?)");

            //Tenemos que mandar la cantidad de datos necesarios para llenar todos los campos de la tabla
            pst.setInt(1, 0);
            pst.setString(2, txtNombre.getText().trim());
            pst.setString(3, txtGrado.getText().trim());
            pst.setString(4, txtSección.getText().trim());
            pst.executeUpdate(); //Ejecutamos la actualización

            txtNombre.setText("");
            txtGrado.setText("");
            txtSección.setText("");

            lblEstado.setText("REGISTRO EXITOSO");

            cn.close(); //Cerramos la conexión
            pst.close(); //Cerramos el objeto de la consulta

        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        //Totalmente validado
        try {
            //Realizamos la conexión a la base de datos
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/practicando", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from estudiantes where ID_Estudiante = (?)");

            //Mandamos a la base de datos el dato que necesita para realizar la búsqueda
            pst.setString(1, txtBuscar.getText().trim());

            //El objeto rs guarda el resultado de la consulta
            ResultSet rs = pst.executeQuery();

            //Si el objeto rs guarda un resultado, la nueva fila actual es válida y devuelve un true
            if (rs.next() == true) {
                txtNombre.setText(rs.getString("NombreEstudiante"));
                txtGrado.setText(rs.getString("Grado"));
                txtSección.setText(rs.getString("Sección"));
                lblEstado.setText("BUSQUEDA EXITOSA");
            } else {
                JOptionPane.showMessageDialog(null, "EL CODIGO NO EXISTE");
            }

            cn.close();
            pst.close();

        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnBuscarActionPerformed

