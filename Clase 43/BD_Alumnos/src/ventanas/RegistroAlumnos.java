    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_ins", "root", "");

            PreparedStatement pst = cn.prepareStatement("select * from alumnos where ID = ?");

            pst.setString(1, txtBuscar.getText().trim()); //Mandamos al objeto pst el dato que se necesita para realizar la consulta

            //Ejecuta la consulta sql en el objeto pst y devuelve un objeto ResultSet generado por la consulta         
            ResultSet rs = pst.executeQuery();

            if (rs.next()) { //Si el objeto rs encontro los datos que buscaba, con el método next se brinca al siguiente paso
                txtNombre.setText(rs.getString("NombreAlumno"));
                txtGrupo.setText(rs.getString("Grupo"));
                lblMensaje.setText("BUSQUEDA EXITOSA");
            } else {
                JOptionPane.showMessageDialog(null, "EL CODIGO NO EXISTE");
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnBuscarActionPerformed

