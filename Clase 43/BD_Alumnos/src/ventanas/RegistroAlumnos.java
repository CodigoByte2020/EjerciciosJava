    private void btnPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPDFActionPerformed
        Document documento = new Document(); //Creamos un objeto de tipo Document que será el encargado de mostrar toda la información de la base de datos

        try {
            String ruta = System.getProperty("user.home"); //Creamos una variable que almacene la dirección del usuario propietario

            //Obtenemos una instancia del archivo pdf escrito, en la ruta indicada
            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Documents/NetBeansProjects/Clase 43/BD_Alumnos/New Pdf/Reportes.pdf"));

            //Insertando una imagen al archivo pdf
            Image imagen = Image.getInstance("src/img/header.png");
            imagen.scaleToFit(800, 200);
            imagen.setAlignment(Chunk.ALIGN_CENTER);

            //Dando formato al texto pdf
            Paragraph párrafo = new Paragraph();
            párrafo.setAlignment(Paragraph.ALIGN_CENTER);
            párrafo.add("Tabla informativa generada por la Geekipedia de Ernesto © \n\n");
            párrafo.setFont(FontFactory.getFont("Cambria", 18, Font.BOLD, BaseColor.BLACK));
            párrafo.add("Alumnos registrados \n\n");

            documento.open();
            documento.add(imagen);
            documento.add(párrafo);

            //Creamos un objeto de tipo PdfPTable para almacenar en cada celda la siguiente información
            PdfPTable tabla = new PdfPTable(3);
            tabla.addCell("Código");
            tabla.addCell("Nombre del Alumno");
            tabla.addCell("Grupo");

            try {
                Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/bd_ins", "root", "");
                PreparedStatement pst = cn.prepareStatement("select * from alumnos");

                ResultSet rs = pst.executeQuery();

                if (rs.next() == true) {

                    do {
                        tabla.addCell(rs.getString(1)); //Agregamos en cada celda de la tabla lo que se encuentra en cada columna de la base de datos
                        tabla.addCell(rs.getString(2));
                        tabla.addCell(rs.getString(3));
                    } while (rs.next() == true);
                    documento.add(tabla); //Agregamos al objeto documento todo lo que se encuentra en el objeto tabla
                }
            } catch (DocumentException | SQLException e) {

            }
            documento.close();
            JOptionPane.showMessageDialog(null, "Reporte creado");
        } catch (DocumentException | HeadlessException | FileNotFoundException e) {

        } catch (IOException e) {

        }
    }//GEN-LAST:event_btnPDFActionPerformed

