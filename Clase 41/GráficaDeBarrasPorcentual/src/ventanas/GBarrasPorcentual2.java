    @Override
    public void paint(Graphics grf) {
        super.paint(grf);

        if (bandera == true) {
            int PorcentajeRojo = Integer.parseInt(txtRojo.getText());
            int PorcentajeVerde = Integer.parseInt(txtVerde.getText());
            int PorcentajeAzul = Integer.parseInt(txtAzul.getText());
            int PorcentajeBlanco = Integer.parseInt(txtBlanco.getText());
            int PorcentajeNegro = Integer.parseInt(txtNegro.getText());

            int TotalPorcentaje = PorcentajeRojo + PorcentajeVerde + PorcentajeAzul + PorcentajeBlanco + PorcentajeNegro;

            if (TotalPorcentaje == 100) {
                int VotosRojo = 5000 * PorcentajeRojo / TotalPorcentaje;
                int VotosVerde = 5000 * PorcentajeVerde / TotalPorcentaje;
                int VotosAzul = 5000 * PorcentajeAzul / TotalPorcentaje;
                int VotosBlanco = 5000 * PorcentajeBlanco / TotalPorcentaje;
                int VotosNegro = 5000 * PorcentajeNegro / TotalPorcentaje;

                int AlturaRojo = VotosRojo * 400 / 5000;
                int AlturaVerde = VotosVerde * 400 / 5000;
                int AlturaAzul = VotosAzul * 400 / 5000;
                int AlturaBlanco = VotosBlanco * 400 / 5000;
                int AlturaNegro = VotosNegro * 400 / 5000;

                grf.setColor(Color.red);
                grf.fillRect(300, 100, 50, AlturaRojo);
                grf.setColor(Color.black);
                grf.drawString("Votos - Rojo: " + VotosRojo, 180, 120);
                
                grf.setColor(Color.green);
                grf.fillRect(300, 100 + AlturaRojo, 50, AlturaVerde);
                grf.setColor(Color.black);
                grf.drawString("Votos - Verde: " + VotosVerde, 180, 120 + AlturaRojo);                
                
                grf.setColor(Color.blue);
                grf.fillRect(300, 100 + AlturaRojo + AlturaVerde, 50, AlturaAzul);
                grf.setColor(Color.black);
                grf.drawString("Votos - Azul: " + VotosAzul, 180, 120 + AlturaRojo + AlturaVerde);
                
                grf.setColor(Color.white);
                grf.fillRect(300, 100 + AlturaRojo + AlturaVerde + AlturaAzul, 50, AlturaBlanco);
                grf.setColor(Color.black);
                grf.drawString("Votos - Blanco: " + VotosBlanco, 180, 120 + AlturaRojo + AlturaVerde + AlturaAzul);                
                
                grf.setColor(Color.black);
                grf.fillRect(300, 100 + AlturaRojo + AlturaVerde + AlturaAzul + AlturaBlanco, 50, AlturaNegro);
                grf.setColor(Color.black);
                grf.drawString("Votos - Negro: " + VotosNegro, 180, 120 + AlturaRojo + AlturaVerde + AlturaAzul + AlturaBlanco);
                
            } else {
                JOptionPane.showMessageDialog(null, "La suma de todos los porcentajes debe sumar 100%");
                txtRojo.setText("");
                txtVerde.setText("");
                txtAzul.setText("");
                txtBlanco.setText("");
                txtNegro.setText("");
                bandera = false;
            }
        }
    }
